   <h1>Diwali</h1>
   <p class="para1"> Diwali, the Hindu "festival of lights," is the best known of Hindu festivals and certainly the brightest. While there are many beliefs regarding this festival, the most popular one is that this day symbolizes the victory of good over evil. Amid the dark skies of autumn, lights illumine homes throughout India and her diaspora, while families celebrate with visits to friends and relatives, gifts to each other, donations to the poor, prayers and feasts. </p>

<p class="para1">Diwali celebrations generally last for five days, beginning on the 14th day of the dark half of the Hindu calendar month of Asvina. (Every Hindu month is divided into a light half, when the moon waxes, and a dark half, when it wanes.) By the Gregorian calendar, Diwali falls in October or November.</p> 

<h2>Bright Beauty</h2>
<p class="para1">Diwali’s name comes from the Sanskrit word “deepawali” which means "row of lights." According to tradition, Diwali celebrates the joyous homecoming of Lord Rama, hero of the epic poem the Ramayana. After 14 years of exile, when Lord Rama and his wife Sita returned to rule their country, their people lit their path with small oil lamps called diye.
During Diwali, these lamps shine in rows along homes and temples adorning windowsills, staircases, and parapets or glow from little boats that float down the rivers. Colorful candles are lit alongside the diyes, while fireworks light up the night skies. </p>

<h2>Feasts and Festivities</h2> 
<p class="para1">Fresh flowers and freshly cleaned homes welcome the days of Diwali. Many families draw a colorful rangoli, a decorative pattern made in rice flour, at the entrance of their home. Friends, family, and neighbors visit to share feasts and festivities as well as little treats such as khil (rice puffs) and patashe (sugar disks). Puja, worship of deities, takes place at homes and at temples with prayers and other offerings. </p>

<p class="para1">Diwali also marks the beginning of a new financial year. Households and businesses begin new accounting in new ledgers, which are often decorated with images of Lakshmi - the goddess of fortune. She is the main deity honored during Diwali. </p>

<h2>Diverse Traditions</h2>
<p class="para1">Like other aspects of Hinduism, the world’s oldest religion, the origins of Diwali are remote. The celebration probably has its roots in ancient harvest festivals. And like Hinduism, observance of Diwali is richly varied among the faiths 800 million adherents. 
Although the Rama tradition is widespread, in some parts of India Diwali honors the marriage of Goddess Lakshmi Lord Vishnu; in others. Yet, in other traditions, it remembers the triumph of Lord Krishna over the demon Naraka. While for most Hindus the worship of Lakshmi is a focus of Diwali, Hindus in Bengal honor the fearsome goddess Kali. Ganesha, the elephant-headed god of wisdom, is also widely honored, as are other gods and goddesses.</p>

<h2>Diwali Mela</h2>
<p class="para1">Diwali Mela or Diwali Fete are extremely popular not just in India but all over the world. These help people to celebrate the popular ‘Festival of Lights’ with gaiety and enthusiasm within their own community. Diwali Mela serves a very important role as the festival of Diwali is celebrated in individual homes. For Hindu community outside India, Diwali Mela is a means to bond with cultural roots and acquaint children to the rich Indian culture and heritage.</p> 


<h2>Diwali Mela Celebrations</h2>
<p class="para1">Diwali Melas have become hugely popular these days and are enjoyed with great enthusiasm in India and in countries where there is a significant Indian diaspora. Cultural shows including Ram Lila and Ravan Dahan are organized by professional artists and children to regale the crowds. Games and other fun-filled activities for children and grownups are usually a part of all Diwali Melas. Additionally, stalls of Indian handicrafts and other trinkets are also set up to let people go on a little shopping spree. Diwali Melas outside India are used to showcase the best of Indian culture; Stalls of pottery making, henna painting on palms etc help people enjoy the spirit of India. Mouth watering foods from all parts of India, including chaats, pakodas, dosas, just to name a few satisfy cravings. And, the customary fireworks are undoubtedly an integral part of Diwali Melas. Joyful competitions are also organized to increase public participation.</p>

